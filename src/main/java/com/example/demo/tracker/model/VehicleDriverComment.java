package com.example.demo.tracker.model;

public class VehicleDriverComment {

    /**
     * tells the comments.
     */
    private String comments;
    /**
     * tells the low position.
     */
    private String lowPosition;

    /**
     * get comments.
     * 
     * @return comments
     */
    public String getComments() {
        return comments;
    }

    /**
     * set comments.
     * 
     * @param comments
     */
    public void setComments(final String comments) {
        this.comments = comments;
    }

    /**
     * gets low position.
     * 
     * @return low position
     */
    public String getLowPosition() {
        return lowPosition;
    }

    /**
     * set low position.
     * 
     * @param lowPosition
     */
    public void setLowPosition(final String lowPosition) {
        this.lowPosition = lowPosition;
    }

}
